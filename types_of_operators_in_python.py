 # addition(+)
print(2 + 3)


# subtraction(-)
print(3 - 1)      


# multiplication(*)
print(2 * 3)


# division(/)
print(3 / 2)


# exponential(**)- We use this operator between the base and exponent values.
print(3 ** 2)    


 # modulus(%)- It gives the remainder when the first number is divided from the second number.
print(3 % 2)


# Floor division operator(//)- This operator will divide the first argument by the second and round the result down to the nearest whole numbe
print(3 // 2)            